import { TFormValidator } from './Validator.types'

type TMinLength = (length: number) => TFormValidator
const minLength: TMinLength = length => value => value.length >= length

type TMaxLength = (length: number) => TFormValidator
const maxLength: TMaxLength = length => value => value.length <= length

type TPattern = (pattern: RegExp | string) => TFormValidator
const pattern: TPattern = pattern => value => new RegExp(pattern).test(value)

type TEmail = () => TFormValidator
const email: TEmail = () => value => emailRegex.test(value)
const emailRegex = /(?:[a-z0-9!#$%&'*+/=?^_`{|}~-]+(?:\.[a-z0-9!#$%&'*+/=?^_`{|}~-]+)*|"(?:[\x01-\x08\x0b\x0c\x0e-\x1f\x21\x23-\x5b\x5d-\x7f]|\\[\x01-\x09\x0b\x0c\x0e-\x7f])*")@(?:(?:[a-z0-9](?:[a-z0-9-]*[a-z0-9])?\.)+[a-z0-9](?:[a-z0-9-]*[a-z0-9])?|\[(?:(?:25[0-5]|2[0-4][0-9]|[01]?[0-9][0-9]?)\.){3}(?:25[0-5]|2[0-4][0-9]|[01]?[0-9][0-9]?|[a-z0-9-]*[a-z0-9]:(?:[\x01-\x08\x0b\x0c\x0e-\x1f\x21-\x5a\x53-\x7f]|\\[\x01-\x09\x0b\x0c\x0e-\x7f])+)\])/

type TURL = () => TFormValidator
const url: TURL = () => value => urlRegex.test(value)
const urlRegex = /((([A-Za-z]{3,9}:(?:\/\/)?)(?:[\-;:&=\+\$,\w]+@)?[A-Za-z0-9\.\-]+|(?:www\.|[\-;:&=\+\$,\w]+@)[A-Za-z0-9\.\-]+)((?:\/[\+~%\/\.\w\-_]*)?\??(?:[\-\+=&;%@\.\w_]*)#?(?:[\.\!\/\\\w]*))?)/

const Validator = {
	maxLength,
	minLength,
	pattern,
	email,
	url
}

export default Validator
